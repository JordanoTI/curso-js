let nome = "Jordano";

switch (nome) {
  case "Jordano":
    console.log("O nome é Jordano");
    break;
  case "João":
    console.log("O nome é João");
  default:
    console.log("O nome não foi encontrado");
    break;
}

if (nome == "Jordano") {
  console.log("O nome é Jordano");
} else {
  console.log("O nome não foi encontrado");
}